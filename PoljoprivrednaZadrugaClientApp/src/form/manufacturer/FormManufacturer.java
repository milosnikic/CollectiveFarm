    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String name = txtName.getText().trim();
        String address = txtAddress.getText().trim();
        String place = txtPlace.getText().trim();

        if (name.isEmpty() || address.isEmpty() || place.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter all information!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            Manufacturer manufacturer = new Manufacturer(name, address, place);
            try {
                Controller.getInstance().addManufacturer(manufacturer);//                Logger.getLogger(FormManufacturer.class.getName()).log(Level.SEVERE, null, ex);
            } catch (Exception ex) {
                Logger.getLogger(FormManufacturer.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(this, "Manufacturer has been saved!", "Success", JOptionPane.INFORMATION_MESSAGE);
            int option = JOptionPane.showConfirmDialog(this, "Add one more manufacturer?", "Add New", JOptionPane.YES_NO_OPTION);
            if (option == 0) {
                txtName.setText("");
                txtAddress.setText("");
                txtPlace.setText("");
            } else {
                this.dispose();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        Manufacturer manufacturer = getManufacturerUpdate();//            Logger.getLogger(FormManufacturer.class.getName()).log(Level.SEVERE, null, ex);
        try {
            Controller.getInstance().updateManufacturer(manufacturer);
        } catch (Exception ex) {
            Logger.getLogger(FormManufacturer.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Manufacturer has been successfully updated!", "Success", JOptionPane.INFORMATION_MESSAGE);
        this.dispose();

    }//GEN-LAST:event_btnUpdateActionPerformed

