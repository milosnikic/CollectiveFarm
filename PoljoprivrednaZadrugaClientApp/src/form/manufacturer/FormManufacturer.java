    private void adjustForm(FormMode mode) {
        switch (mode) {
            case NEW:
                lblID.setVisible(false);
                txtID.setVisible(false);
                txtAddress.setEnabled(true);
                txtName.setEnabled(true);
                txtPlace.setEnabled(true);

                btnEdit.setVisible(false);
                btnUpdate.setVisible(false);
                btnSave.setVisible(true);

                clearForm();
                setTitle("New Manufacturer");
                break;
            case EDIT:
                txtID.setEnabled(false);
                txtAddress.setEnabled(true);
                txtName.setEnabled(true);
                txtPlace.setEnabled(true);

                btnEdit.setVisible(false);
                btnUpdate.setVisible(true);
                btnSave.setVisible(false);

                setTitle("Edit Manufacturer");
                break;
            case VIEW:
                txtID.setEnabled(false);
                txtAddress.setEnabled(false);
                txtName.setEnabled(false);
                txtPlace.setEnabled(false);

                btnEdit.setVisible(true);
                btnUpdate.setVisible(false);
                btnSave.setVisible(false);

                setTitle("View Manufacturer");
                break;
        }
    }

    private void clearForm() {
        txtID.setText("");
        txtAddress.setText("");
        txtName.setText("");
        txtPlace.setText("");
    }

    private void populateForm(Manufacturer manufacturer) {
        txtID.setText(String.valueOf(manufacturer.getId()));
        txtAddress.setText(manufacturer.getAddress());
        txtName.setText(manufacturer.getName());
        txtPlace.setText(manufacturer.getPlace());
    }

    private void setCenter() {
        this.setLocationRelativeTo(null);
    }

    private Manufacturer getManufacturerUpdate() {
        Long id = new Long(txtID.getText());
        String address = txtAddress.getText().trim();
        String name = txtName.getText().trim();
        String place = txtPlace.getText().trim();
        return new Manufacturer(id, name, address, place);
    }
