    private void populateForm() {
        if (!Configuration.getInstance().getProperty("url").isEmpty()) {
            txtUrl.setText(Configuration.getInstance().getProperty("url"));
        }
        if (!Configuration.getInstance().getProperty("username").isEmpty()) {
            txtUsername.setText(Configuration.getInstance().getProperty("username"));
        }
        if (!Configuration.getInstance().getProperty("password").isEmpty()) {
            txtPassword.setText(Configuration.getInstance().getProperty("password"));
        }
    }
