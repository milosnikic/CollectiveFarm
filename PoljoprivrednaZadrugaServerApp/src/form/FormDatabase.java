    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String url = txtUrl.getText().trim();
        String username = txtUsername.getText().trim();
        String password = String.valueOf(txtPassword.getPassword());
        
        try {
            Connection connection = DriverManager.getConnection(url, username, password);
            Configuration.getInstance().setProperty("url", url);
            Configuration.getInstance().setProperty("username", username);
            Configuration.getInstance().setProperty("password", password);
            
            Configuration.getInstance().saveChanges();
            connection.close();
            JOptionPane.showMessageDialog(this, "Parametri su uspesno sacuvani", "Info", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } catch (SQLException ex) {
            Logger.getLogger(FormDatabase.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Molimo unesite validne parametre", "Error", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

